part of 'employee_repo_impl.dart';

mixin _EmployeesRepoEmployeeCreateMixin
    on _EmployeesRepoEmployeeCreateRpcMixin, _EmployeesRepoCreateFallbackRequestMixin {
  @override
  Future<String> createEmployee({
    required String fullName,
    required String email,
    required String phone,
    String? photoUrl,
    required String nationalId,
    DateTime? dateOfBirth, String? gender, String? nationality, String? education,
    DateTime? nationalIdExpiry,
    String? notes, String? managerId, String? maritalStatus,
    String? address, String? city, String? country,
    String? passportNo,
    DateTime? passportExpiry, DateTime? residencyIssueDate, DateTime? residencyExpiryDate,
    DateTime? insuranceStartDate, DateTime? insuranceExpiryDate,
    String? insuranceProvider, String? insurancePolicyNo,
    String? educationLevel, String? major, String? university,
    required String departmentId,
    required String jobTitleId,
    required DateTime hireDate,
    required String employmentType,
    required String contractType,
    required DateTime contractStart,
    DateTime? contractEnd, int? probationMonths, String? contractFileUrl,
    required double basicSalary,
    required double housingAllowance,
    required double transportAllowance,
    required double otherAllowance,
    String? bankName, String? iban, String? accountNumber, String? paymentMethod,
  }) async {
    final tenantId = await _tenantId();
    final effectivePhotoUrl = _normalizePhotoUrl(photoUrl);
    final effectiveManagerId = await _resolveDepartmentManagerId(
      tenantId: tenantId,
      departmentId: departmentId,
      managerId: managerId,
    );

    try {
      final createdEmployeeId = await _createEmployeeViaRpc(
        tenantId: tenantId,
        fullName: fullName,
        email: email,
        params: _createEmployeeRpcParams(
          tenantId: tenantId,
          fullName: fullName,
          email: email,
          phone: phone,
          nationalId: nationalId,
          departmentId: departmentId,
          jobTitleId: jobTitleId,
          hireDate: hireDate,
          employmentType: employmentType,
          contractType: contractType,
          contractStart: contractStart,
          basicSalary: basicSalary,
          housingAllowance: housingAllowance,
          transportAllowance: transportAllowance,
          otherAllowance: otherAllowance,
          photoUrl: effectivePhotoUrl,
          dateOfBirth: dateOfBirth,
          gender: gender,
          nationality: nationality,
          education: education,
          nationalIdExpiry: nationalIdExpiry,
          notes: notes,
          managerId: effectiveManagerId,
          maritalStatus: maritalStatus,
          address: address,
          city: city,
          country: country,
          passportNo: passportNo,
          passportExpiry: passportExpiry,
          residencyIssueDate: residencyIssueDate,
          residencyExpiryDate: residencyExpiryDate,
          insuranceStartDate: insuranceStartDate,
          insuranceExpiryDate: insuranceExpiryDate,
          insuranceProvider: insuranceProvider,
          insurancePolicyNo: insurancePolicyNo,
          educationLevel: educationLevel,
          major: major,
          university: university,
          contractEnd: contractEnd,
          probationMonths: probationMonths,
          contractFileUrl: contractFileUrl,
          bankName: bankName,
          iban: iban,
          accountNumber: accountNumber,
          paymentMethod: paymentMethod,
        ),
      );
      if (createdEmployeeId != null) return createdEmployeeId;
    } catch (_) {}

    return _createEmployeeFallbackForRequest(
      tenantId: tenantId,
      fullName: fullName,
      email: email,
      phone: phone,
      nationalId: nationalId,
      departmentId: departmentId,
      jobTitleId: jobTitleId,
      hireDate: hireDate,
      employmentType: employmentType,
      contractType: contractType,
      contractStart: contractStart,
      basicSalary: basicSalary,
      housingAllowance: housingAllowance,
      transportAllowance: transportAllowance,
      otherAllowance: otherAllowance,
      photoUrl: effectivePhotoUrl,
      dateOfBirth: dateOfBirth,
      gender: gender,
      nationality: nationality,
      education: education,
      nationalIdExpiry: nationalIdExpiry,
      notes: notes,
      managerId: effectiveManagerId,
      maritalStatus: maritalStatus,
      address: address,
      city: city,
      country: country,
      passportNo: passportNo,
      passportExpiry: passportExpiry,
      residencyIssueDate: residencyIssueDate,
      residencyExpiryDate: residencyExpiryDate,
      insuranceStartDate: insuranceStartDate,
      insuranceExpiryDate: insuranceExpiryDate,
      insuranceProvider: insuranceProvider,
      insurancePolicyNo: insurancePolicyNo,
      educationLevel: educationLevel,
      major: major,
      university: university,
      contractEnd: contractEnd,
      probationMonths: probationMonths,
      contractFileUrl: contractFileUrl,
      bankName: bankName,
      iban: iban,
      accountNumber: accountNumber,
      paymentMethod: paymentMethod,
    );
  }
}
